package com.itheima.po;

public class TravelAgency {
	private Integer agenid;
	private String agenname;
	private String agenaddress;
	
	public Integer getAgenid() {
		return agenid;
	}
	public void setAgenid(Integer agenid) {
		this.agenid = agenid;
	}
	public String getAgenname() {
		return agenname;
	}
	public void setAgenname(String agenname) {
		this.agenname = agenname;
	}
	public String getAgenaddress() {
		return agenaddress;
	}
	public void setAgenaddress(String agenaddress) {
		this.agenaddress = agenaddress;
	}
}
